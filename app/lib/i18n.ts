export function normalizeLanguageCode(value: string | null | undefined): LanguageCode {
  const normalized = (value || "").trim().toLowerCase().split("-")[0];
  return SUPPORTED_LANGUAGES.some((language) => language.code === normalized)
    ? (normalized as LanguageCode)
    : DEFAULT_LANGUAGE;
}

export function getLanguageMeta(code: LanguageCode): LanguageMeta {
  return SUPPORTED_LANGUAGES.find((language) => language.code === code) || SUPPORTED_LANGUAGES[0];
}

export function translate(code: LanguageCode, key: string) {
  return messages[code]?.[key] || messages[DEFAULT_LANGUAGE]?.[key] || key;
}
